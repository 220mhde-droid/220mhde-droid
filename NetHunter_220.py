import os

def network_radar():
    print("--- رادار الشبكات | مختبر علاء الميداني ---")
    print("[!] جاري البحث عن إشارات الإنترنت المتاحة...")
    
    # هذا الأمر يقوم باستدعاء قدرات الهاتف لمسح الشبكات المحيطة
    # ملاحظة: يعمل بشكل أفضل مع صلاحيات المطور
    cmd = os.popen('cmd-iw dev wlan0 scan | grep SSID').read()
    
    if cmd:
        print("\n[✓] تم رصد الشبكات التالية في محيطك:")
        print(cmd)
    else:
        print("\n[X] لم يتم العثور على شبكات مفتوحة حالياً. جرب التحرك لمكان أعلى.")

if __name__ == "__main__":
    network_radar()